package Operators;

public class RelationalMain {

	public static void main(String[] args) {

		// create variables
		int a=7,b=11;
		
		//value of a & b
		System.out.println("a is " +a +" and b is " +b);
		
		// == operator
		System.out.println(a==b);
		
		// ! operator
		System.out.println(a!=b);
		
		// > operator
		System.out.println(a>b);
		
		// < operator
		System.out.println(a<b);
		
		// >= operator
		System.out.println(a>=b);
		
		// <= operator
		System.out.println(a<=b);


	}

}
/* Relational operators are used in decision making and loops. */
